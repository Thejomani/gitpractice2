package Java_Grooming;

public class primenumber 
{

	public static void main(String[] args) 
	{
		int no=5;
		int count=0;
		for(int i=1;i<=no;i++);
		{
			if(no%1==0);
			{
				count++;
			}
		}
			if(count==2)
			{
				System.out.println("it is a primenumber");
			}
			else
			{
				System.out.println("it is not a primenumber");
		}

	}

}
